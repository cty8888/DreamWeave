// 清空所有业务数据，确保"只要干净数据"；随后重建预设标签。
db.pragma('foreign_keys = OFF');
for (const tbl of [
  'notifications', 'comments', 'likes', 'favorites', 'continuations',
  'fragments', 'follows', 'dreams', 'scene_tags', 'emotion_tags', 'users',
]) {
  db.exec(`DELETE FROM ${tbl}`);
  db.exec(`DELETE FROM sqlite_sequence WHERE name = '${tbl}'`); // 重置自增 id
}
db.pragma('foreign_keys = ON');
seed(); // 重新写入预设标签
console.log('已清空旧数据，开始重建干净生态…');

const pwHash = bcrypt.hashSync(PASSWORD, 10);
const now = () => "datetime('now', '-' || ? || ' minutes')";

const insertUser = db.prepare('INSERT INTO users (username, password_hash) VALUES (?, ?)');
const insertDream = db.prepare(
  `INSERT INTO dreams (user_id, title, content, scene_ids, emotion_tags, visibility, ai_story, interpretation, status, created_at)
   VALUES (@user_id, @title, @content, @scene_ids, @emotion_tags, @visibility, @ai_story, @interpretation, @status, datetime('now', @age))`
);
const insertCont = db.prepare(
  `INSERT INTO continuations (dream_id, user_id, parent_id, content, is_independent, created_at)
   VALUES (?, ?, ?, ?, ?, datetime('now', ?))`
);
const insertLike = db.prepare('INSERT OR IGNORE INTO likes (user_id, dream_id) VALUES (?, ?)');
const insertComment = db.prepare(
  `INSERT INTO comments (dream_id, user_id, content, created_at) VALUES (?, ?, ?, datetime('now', ?))`
);
const insertFollow = db.prepare('INSERT OR IGNORE INTO follows (follower_id, following_id) VALUES (?, ?)');
const insertFav = db.prepare('INSERT OR IGNORE INTO favorites (user_id, dream_id) VALUES (?, ?)');
const insertFrag = db.prepare(
  `INSERT INTO fragments (user_id, content, sort_order, session_id, created_at) VALUES (?, ?, ?, ?, datetime('now', ?))`
);
const insertNotif = db.prepare(
  `INSERT INTO notifications (user_id, actor_id, type, dream_id, is_read, created_at)
   VALUES (?, ?, ?, ?, ?, datetime('now', ?))`
);

const { v4: uuidv4 } = require('uuid');

const stats = { users: 0, dreams: 0, continuations: 0, comments: 0, likes: 0, follows: 0, favorites: 0, fragments: 0, notifications: 0 };

const run = db.transaction(() => {
  // 1) 用户
  const userIds = USERNAMES.map((name) => {
    const r = insertUser.run(name, pwHash);
    stats.users++;
    return r.lastInsertRowid;
  });

  function notify(recipientId, actorId, type, dreamId, ageMin) {
    if (!recipientId || recipientId === actorId) return;
    insertNotif.run(recipientId, actorId, type, dreamId || null, chance(0.5) ? 1 : 0, `-${ageMin} minutes`);
    stats.notifications++;
  }

  // 2) 梦境（打乱后依次分配给用户，保证每条内容唯一）
  const dreamPool = sample(DREAMS, DREAMS.length);
  const dreamRows = [];
  dreamPool.forEach((d, i) => {
    const owner = userIds[i % userIds.length];
    const visibility = chance(0.72) ? 'public' : 'private';
    const ageMin = intBetween(30, 60 * 24 * 30); // 近 30 天内
    const r = insertDream.run({
      user_id: owner,
      title: d.t,
      content: d.c,
      scene_ids: JSON.stringify(d.s),
      emotion_tags: JSON.stringify((d.e || []).map(([name, intensity]) => ({ name, intensity }))),
      visibility,
      ai_story: d.story || null,
      interpretation: d.interp || null,
      status: d.story ? 'completed' : 'draft',
      age: `-${ageMin} minutes`,
    });
    stats.dreams++;
    dreamRows.push({ id: r.lastInsertRowid, owner, visibility, ageMin });
  });

  const publicDreams = dreamRows.filter((d) => d.visibility === 'public');

  // 3) 关注关系
  userIds.forEach((uid) => {
    const others = userIds.filter((x) => x !== uid);
    sample(others, intBetween(2, 7)).forEach((target) => {
      if (insertFollow.run(uid, target).changes > 0) {
        stats.follows++;
        notify(target, uid, 'follow', null, intBetween(10, 60 * 24 * 20));
      }
    });
  });

  // 4) 每条公开梦境：点赞 / 评论 / 接力
  publicDreams.forEach((dr) => {
    const candidates = userIds.filter((u) => u !== dr.owner);

    // 点赞
    sample(candidates, intBetween(0, Math.min(14, candidates.length))).forEach((uid) => {
      if (insertLike.run(uid, dr.id).changes > 0) {
        stats.likes++;
        if (chance(0.6)) notify(dr.owner, uid, 'like', dr.id, intBetween(5, dr.ageMin));
      }
    });

    // 评论
    sample(candidates, intBetween(0, 4)).forEach((uid) => {
      const age = intBetween(5, Math.max(6, dr.ageMin));
      insertComment.run(dr.id, uid, pick(COMMENTS), `-${age} minutes`);
      stats.comments++;
      notify(dr.owner, uid, 'comment', dr.id, age);
    });

    // 接力续写（线性链 + 偶尔独立）
    let parent = null;
    const n = intBetween(0, 3);
    for (let k = 0; k < n; k++) {
      const author = pick(candidates);
      const independent = chance(0.3);
      const age = intBetween(5, Math.max(6, dr.ageMin));
      const r = insertCont.run(dr.id, author, independent ? null : parent, pick(CONTINUATIONS), independent ? 1 : 0, `-${age} minutes`);
      stats.continuations++;
      if (!independent) parent = r.lastInsertRowid;
      notify(dr.owner, author, 'continue', dr.id, age);
    }
  });

  // 5) 收藏
  userIds.forEach((uid) => {
    sample(publicDreams.filter((d) => d.owner !== uid), intBetween(1, 5)).forEach((dr) => {
      if (insertFav.run(uid, dr.id).changes > 0) stats.favorites++;
    });
  });

  // 6) 碎片串联（约一半用户各有 1~2 组）
  userIds.forEach((uid) => {
    const sessions = chance(0.5) ? intBetween(1, 2) : 0;
    for (let sIdx = 0; sIdx < sessions; sIdx++) {
      const sid = uuidv4();
      const frags = sample(FRAGMENTS, intBetween(2, 4));
      const age = intBetween(20, 60 * 24 * 15);
      frags.forEach((c, idx) => {
        insertFrag.run(uid, c, idx, sid, `-${age} minutes`);
        stats.fragments++;
      });
    }
  });

  return userIds;
});

run();

